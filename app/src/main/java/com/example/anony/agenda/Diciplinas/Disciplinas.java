public class Disciplinas {
    private String Disc_nome;
    private String Disc_abreviacao;
    private String Disc_Prof;
    private long id;


    public String getDisc_nome() {
        return Disc_nome;
    }
    public void setDisc_nome(String Disc_nome) {
        this.Disc_nome = Disc_nome;
    }
    public String getDisc_abreviacao() {
        return Disc_abreviacao;
    }
    public void setDisc_abreviacao(String Disc_abreviacao) {
        this.Disc_abreviacao = Disc_abreviacao;
    }
    public String getDisc_Prof() {
        return Disc_Prof;
    }
    public void setDisc_Prof(String Disc_Prof ) {
        this.Disc_Prof = Disc_Prof;
    }
    public long getId() {
        return id;
    }
    public void setId(long id) {
        this.id = id;
    }
}
